#include <iostream>
using namespace std;
int main()
{
    int A;
    int B;
    cout << "Enter The First Number\n";
    cin >> A;
    cout << "Enter The Second Number\n";
    cin >> B;
    cout<< A << " + "<< B <<" = "<< A+B<<endl;
    cout<< A << " - "<< B <<" = "<< A-B<<endl;
    cout<< A << " * "<< B <<" = "<< A*B<<endl;
    cout<< A << " / "<< B <<" = "<< A/B<<endl;
    return 0 ;
}